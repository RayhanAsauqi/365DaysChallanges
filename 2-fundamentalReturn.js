/**
 * Fungsi untuk menambahkan dua angka.
 * @param {number} a - Angka pertama.
 * @param {number} b - Angka kedua.
 * @returns {number} - Hasil penjumlahan dari a dan b.
 */
function add(a, b) {
  // mengembalikan nilai penjumlahannya
  return a + b;
}

/**
 * Fungsi untuk mengalikan dua angka.
 * @param {number} a - Angka pertama.
 * @param {number} b - Angka kedua.
 * @returns {number} - Hasil perkalian dari a dan b.
 */
function multiply(a, b) {
  // mengembalikan nilai perkaliannya
  return a * b;
}

/**
 * Fungsi untuk membagi dua angka.
 * @param {number} a - Angka yang akan dibagi (dividen).
 * @param {number} b - Angka pembagi (divisor).
 * @returns {number} - Hasil pembagian dari a dibagi dengan b.
 */
function divide(a, b) {
  //mengembalikan nilai pembagiannya
  return a / b;
}

/**
 * Fungsi untuk mendapatkan sisa pembagian dari dua angka.
 * @param {number} a - Angka yang akan dibagi (dividen).
 * @param {number} b - Angka pembagi (divisor).
 * @returns {number} - Sisa pembagian dari a dibagi dengan b.
 */
function mod(a, b) {
  //mengembalikan nilai sisa pembagiannya
  return a % b;
}

/**
 * Fungsi untuk melakukan operasi eksponensial pada dua angka.
 * @param {number} a - Angka yang akan dipangkatkan (basis).
 * @param {number} b - Angka pangkat.
 * @returns {number} - Hasil dari a dipangkatkan dengan b.
 */
function exponent(a, b) {
  //mengembalikan  nilai yang di pangkatkan
  return Math.pow(a, b);
}

/**
 * Fungsi untuk mengurangkan dua angka.
 * @param {number} a - Angka yang akan dikurangkan (minuend).
 * @param {number} b - Angka yang akan dikurangkan (subtrahend).
 * @returns {number} - Selisih dari a dikurangkan dengan b.
 */
function subt(a, b) {
  // mengembalikan nilai yang dikurangkan
  return a - b;
}
